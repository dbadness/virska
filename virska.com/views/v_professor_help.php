<div class="hHeader">
	<h3>Adding Events to Your Sections</h3>
</div>
<div class="hCopy">
	<p>An "event" is a happening inside of your sections. It can be anything that is due on a date. An assignment ("Paper 1 Due"), an actually event ("Guest Lecturer from MIT"), a pizza party - an event is anything you decide and because students view your sections by day, they can see all of there events, from all of their professors, in one place.	</p>
	<p>To add an event, start on the <a href="/professor/dashbaord">Dashboard page</a> and to the right of the list of your sections that you've created, click on the "View" link. That will bring you to the page displayed on the right, and you can give the event a brief description, attach a document for your students to download to aid with the event, and even allow them to submit documents to you by clicking on the "Allow Submissions" button.</p>
</div>
