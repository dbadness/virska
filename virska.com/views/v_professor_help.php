<div class="hHeader">
	<h3>Adding Classes and Sections</h3>
</div>
<div class="hCopy">
	<p>To add a class or section, go to the Add Classes page with can be found by clicking on the "Add or Delete Classes and Sections" link on the <a href="/professor/dashbaord">Dashboard page</a>.</p>
	<p>From there, (it's displayed on the right), you can input your classes' information so students know what your class is called and when and where it meets.</p>
