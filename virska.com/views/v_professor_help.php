<div class="hHeader">
	<h3>Viewing and Grading Submissions</h3>
</div>
<div class="hCopy">
	<p>When you enable the "Allow Submissions" feature when you create an event, students are able to upload their assignments for you to view, download, and grade.</p>
	<p>To see these submissions, go to the <a href="/professor/dashbaord">Dashboard page</a>, click on the "View" link to the right of the section in which the submission is related, and you'll arrive at the top picture on the right. For every event that you created with the "Allow Submissions" feature enabled, you can click on the "View" link in that event (where the red arrow is pointing), which displays the bottom picture.</p>
	<p>All of the students' submissions will be listed for you, and you can download their submissions by clicking on the blue document icon, give them feedback on their submission, and give them a grade. When you click "Publish to this Student," the information you provided will be available for the student to see. After you publish your grade and feedback, Virska saves it for you in this view so you can always change it should you have to. When you're finished with your grading, remember to message your students so they know to check out their grades! (Don't worry - we'll cover messaging next.)</p>
</div>
